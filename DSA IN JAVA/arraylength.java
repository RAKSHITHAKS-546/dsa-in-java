public class arraylength {
    public static void main(String[] args) {
        int n;
        int[] arr={2,3,4,5,6,789,23,3,34,4,5,5};
        System.out.println("Length of the array : "+arr.length);
        n=arr.length;
        int i=0;
        for(i=0;i<=n;i++)
        {
            System.out.println(arr[i]+" ");
        }
    }
    
}
